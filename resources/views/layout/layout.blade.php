       function update(qty, rowId, number_db){
            var _token = $('input[name="_token"]').val();
            if(qty>number_db){
                swal("Xin lỗi!", "Số lượng chúng tôi chỉ còn "+ number_db + " sản phẩm", "error");
            }else{
                $.ajax({
                    url: '{{url('/update')}}',
                    method: 'POST',
                    data: {
                        qty:qty,
                        rowId:rowId,
                        _token:_token
                    },
                    success:function(data){
                        location.reload();
                    }
                })
            }    
       }


